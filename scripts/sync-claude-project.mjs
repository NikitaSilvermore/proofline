// Regenerates the Milestamp Claude-project upload folder from the CANONICAL docs.
//
// The workflow this repo runs on: explore ideas in the Milestamp *project chat*,
// implement them here in the *code chat*, and keep both mirrored. The project
// attachments are a SECOND CORPUS — no git behind them, they drift silently, and
// a stale attachment silently poisons every chat that reads it (see AGENTS.md →
// "THE SECOND CORPUS"). This script removes that risk: the upload folder is a
// pure MIRROR of the canonical files, wiped and rebuilt every run. Never
// hand-edit the folder — edit the source doc and re-run this.
//
// Usage:  node scripts/sync-claude-project.mjs
// Then:   upload the whole  milestamp-claude-project/  folder to the Milestamp
//         Claude project (replacing what's there), from the current working tree.
//
// The folder is gitignored (a regenerable build artifact); this script is the
// source of truth for WHAT is attached — edit the lists below to change the set.
// Nothing HISTORICAL is ever included: docs/historical/* is never in these lists,
// so a superseded doc can never reach the project without its banner.

import { mkdirSync, rmSync, copyFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";
import { join, dirname, basename } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT = join(root, "milestamp-claude-project");

// Canonical docs — governing (LAW/BRAIN/SPEC), orientation, and GTM reference.
// Post-reorg paths (docs live under docs/; AGENTS + README stay at root).
const DOCS = [
  // — governing —
  "AGENTS.md",                      // BRAIN — living briefing + handoff
  "docs/BUILD_SPEC.md",             // LAW   — frozen spec
  "docs/PRICING.md",                // LAW   — pricing source of truth
  "docs/RISK-REGISTER.md",          // SPEC  — threat register + strike-order
  "docs/DECISIONS.md",              // BRAIN — running decision log
  // — orientation —
  "README.md",                      // REFERENCE — orientation / repo map
  // — GTM reference (labelled REFERENCE: drafts + plan, not decisions) —
  "docs/marketing/POSTING-KIT.md",
  "docs/marketing/captions.md",
];

// Design contract: the demo mockups, kept in a mockups/ subfolder.
const MOCKUP_GLOB_DIR = "docs/mockups"; // all *.html (the three NLS demos)

// wipe + recreate
rmSync(OUT, { recursive: true, force: true });
mkdirSync(OUT, { recursive: true });
mkdirSync(join(OUT, "mockups"), { recursive: true });

let n = 0;
const missing = [];
for (const rel of DOCS) {
  const src = join(root, rel);
  if (!existsSync(src)) { missing.push(rel); continue; }
  copyFileSync(src, join(OUT, basename(rel)));
  n++;
}

let m = 0;
for (const f of readdirSync(join(root, MOCKUP_GLOB_DIR))) {
  if (!f.endsWith(".html")) continue;
  copyFileSync(join(root, MOCKUP_GLOB_DIR, f), join(OUT, "mockups", f));
  m++;
}

writeFileSync(join(OUT, "README-UPLOAD.md"),
`# Milestamp — Claude project upload set (GENERATED — do not hand-edit)

Regenerated by \`scripts/sync-claude-project.mjs\` from the canonical repo docs.
Upload this whole folder to the Milestamp Claude project (replace what's there)
so every project chat has the full, current design knowledge.

- ${n} docs (governing + orientation + GTM reference) at the top level
- ${m} mockups (the NLS demo HTML — the visual contract) under \`mockups/\`

**Never edit files here** — they are copies. Edit the source doc in the repo and
re-run the script. If a doc changed, the old attachment MUST be replaced (stale
attachments silently poison chats — this is why the folder is always rebuilt).
Nothing HISTORICAL is ever included.
`);

console.log(`synced → milestamp-claude-project/  (${n} docs + ${m} mockups)`);
if (missing.length) console.log("⚠ missing (not copied):", missing.join(", "));
console.log("Now upload the whole milestamp-claude-project/ folder to the Milestamp Claude project (replace what's there).");
